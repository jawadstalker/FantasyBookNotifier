import stanza

stanza.download('fa')

nlp = stanza.Pipeline('fa', processors='tokenize,ner')

text = "باران آرامی می‌بارید و سارا زیر چتر قرمز کوچک خود قدم می‌زد. امین از کنار درختی بیرون آمد و با لبخندی گفت: «سلام، نمی‌خواستم تو را خیس ببینم.» رضا که کمی عقب‌تر ایستاده بود، دفترچه‌ای در دست داشت و نگاهی کنجکاو به آن‌ها انداخت. سارا کمی خجالت‌زده جواب داد: «من خوبم، ممنون.» امین دستش را دراز کرد تا چترش را بیشتر کند و هر سه زیر قطره‌های باران به سمت کافه‌ای قدیمی رفتند. رضا ناگهان گفت: «این‌جا همیشه پر از قصه‌های عجیب است.» سارا با هیجان پرسید: «قصه‌هایی مثل چه؟» امین نگاهی به پنجره‌های بخارگرفته انداخت و گفت: «مثل رازهایی که هیچ‌کس جرات ندارد فاش کند.» رضا دفترچه‌اش را باز کرد و شروع کرد به خواندن نوشته‌ای قدیمی که سه نفر را به دنیایی عجیب دعوت می‌کرد. صدای رعدی بلند شد و هر سه لحظه‌ای مکث کردند، انگار چیزی فراتر از باران در انتظارشان بود. وقتی وارد کافه شدند، دنیا برای آن‌ها دیگر هیچ‌گاه مثل قبل نبود."
doc = nlp(text)

for ent in doc.ents:
    if ent.type == 'PER':  
        print(ent.text)
